/*

FILAS - Estruturas DE DADOS LINEARES

Uma fila é uma estrutura de dados linear que segue o princípio FIFO (First In, First Out),
onde o primeiro elemento a ser inserido é o primeiro a ser removido.
As filas são usadas em diversas aplicações, como gerenciamento de tarefas, impressão de documentos,
e simulação de processos.

VETORES - Estruturas DE DADOS LINEARES

Uma vetor (ou array) é uma estrutura de dados linear que armazena elementos em posições contíguas de memória.
Cada elemento pode ser acessado diretamente através de um índice, permitindo acesso rápido e eficiente.



====================================================

PILHAS - Estruturas DE DADOS LINEARES

Uma pilha é uma estrutura de dados linear que segue o princípio LIFO (Last In, First Out),
onde o último elemento a ser inserido é o primeiro a ser removido.
As pilhas são usadas em diversas aplicações, como gerenciamento de chamadas de função,
desfazer/refazer operações em editores, e avaliação de expressões matemáticas.


*/

